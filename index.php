			<h2 style="background: url(images/prix-13euros.png) no-repeat right bottom;">n°3 : « <strong>Spip pratique 1.9</strong> »</h2>

			<p>Spip est un système de publication pour l’Internet, dont l’attachement à la publication collaborative n’est plus à démontrer..</p>
			<p>Spip pratique 1.9 est un livre destiné à tous, de la prise en main de celui qui ne connaît absolument pas ce logiciel, jusqu’à un niveau avancé de connaissances. Il s'adresse aussi bien aux rédacteurs qu'aux administrateurs, pour un usage de base ou complet.</p>
			<p>De manière simple et conviviale vous trouverez réunis dans cet ouvrage tous les trucs et astuces  pour une parfaite utilisation de Spip.</p>
			<p>Brèves, articles, gestions de documents et images, forums... toutes les fonctionnalités de Spip sont abordées et clairement explicitées.</p>
			<p>On y aborde notamment les points suivants : présentation et utilisation de l'interface web intuitive (qui facilite la proposition d’articles et de brèves ainsi que la gestion éditoriale du site), les raccourcis typographique qui permettent de mettre en forme un texte sans avoir à utiliser le langage HTML (rendant ainsi la contribution rédactionnelle accessible à tous), la gestion des droits et accès des rédacteurs et administrateurs et la modération des forums, sans oublier la possibilité d'insérer des documents comme des images, des vidéos, du son, etc.</p>                    
                       
