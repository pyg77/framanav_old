			<h3>Le logiciel</h3>
			<p>Le système de gestion de contenu Spip est l'un des logiciels de création de site Internet les plus diffusés dans le monde francophone d'où il est issu. Il est réputé pour être d'une grande simplicité d'installation, d'usage et de maintenance. Son orientation collaborative le destine tout particulièrement aux structures associatives et aux institutions publiques ou privées.</p>
